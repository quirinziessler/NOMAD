export function resolveCountryCodeForAirport(airportCode: string | null): string | null {
  if (!airportCode) return null;
  const code = airportCode.trim().toUpperCase();
  if (!/^[A-Z]{3}$/.test(code)) return null;
  return AIRPORT_TO_COUNTRY[code] ?? null;
}

export function getCountriesFromFlightLocation(location: string | null): string[] {
  if (!location) return [];
  const match = location.match(/\b([A-Z]{3})\b\s*(?:→|->|-)\s*\b([A-Z]{3})\b/);
  if (!match) return [];
  const out: string[] = [];
  const from = AIRPORT_TO_COUNTRY[match[1]];
  const to = AIRPORT_TO_COUNTRY[match[2]];
  if (from) out.push(from);
  if (to && to !== from) out.push(to);
  return out;
}
